export const getDataOfGTAColorByRGB = (color: RGB) => {
  return gtaColors.find(c => c.color.r === color.r && c.color.g === color.g && c.color.b === color.b);
};

export const getDataOfGTAColorById = (id: number) => {
  return gtaColors.find(c => c.id === id);
};

export const getRGBOfColor = (color: RGB | number) => {
  return typeof color === 'number' ? getDataOfGTAColorById(color)?.color ?? { r: 0, g: 0, b: 0 } : color;
};
