export const getNearestZoneAtCoords = (coords: Vec3) => {
  let nearestZone: Vec3 | null = null;
  let nearestDist: number = -1;
  const coordVec = Vector3.create(coords);
  for (const zone of ZONE_INFO) {
    if (nearestDist === -1) {
      nearestZone = zone.origin;
      continue;
    }
    const dist = coordVec.distance(zone.origin);
    if (nearestDist > dist) {
      nearestZone = zone.origin;
      nearestDist = dist;
      continue;
    }
  }
  return nearestZone;
};
